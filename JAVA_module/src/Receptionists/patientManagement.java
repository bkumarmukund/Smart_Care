    public void loadData() {
        connection = Connector.ConnectDb();
        String sql = "select patientID, FN, MN, LN, age, dob, phone, email from patient";
        try {
            ps = connection.prepareStatement(sql);
            rs = ps.executeQuery();
            Object columnData[] = new Object[11];
            while (rs.next()) {
                columnData[0] = rs.getString("FN");
                columnData[1] = rs.getString("MN");
                columnData[2] = rs.getString("LN");
                columnData[3] = rs.getInt("age");
                columnData[4] = rs.getString("dob");
                columnData[5] = rs.getString("phone");
                columnData[6] = rs.getString("email");
                columnData[7] = rs.getString("patientID");
                defaultTableModel.addRow(columnData);
            }
        } catch (SQLException e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, e);
        }
    }

