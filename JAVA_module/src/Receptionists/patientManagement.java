        String middleName = (pMTable.getValueAt(row, 1).toString());
        String lastName = (pMTable.getValueAt(row, 2).toString());
        int oldage = Integer.parseInt((pMTable.getValueAt(row, 3).toString()));
        String dateofB = (pMTable.getValueAt(row, 4).toString());
